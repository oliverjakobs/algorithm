void test_des()
{
    const byte M[] = { 0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF };
    const byte K[] = { 0x13, 0x34, 0x57, 0x79, 0x9B, 0xBC, 0xDF, 0xF1 };
    const byte E[] = { 0x85, 0xE8, 0x13, 0x54, 0x0F, 0x0A, 0xB4, 0x05 };
    const byte K3[] = { 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 
                        0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 
                        0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17 };
    const byte E3[] = { 0xa2, 0xf7, 0xdc, 0x8b, 0xcb, 0x6e, 0xc0, 0x0c };

    byte C[8];
    byte D[8];

    printf("--| DES |--------\n");

    TEST_CASE("Encrypt");
    TEST_CHECK_ARRAY(des_encrypt_block(M, C, K), E);
    TEST_CASE_EVAL();

    TEST_CASE("Decrypt");
    TEST_CHECK_ARRAY(des_decrypt_block(C, D, K), M);
    TEST_CASE_EVAL();

    TEST_CASE("Encrypt TDES");
    TEST_CHECK_ARRAY(tdes_encrypt_block(M, C, K3), E3);
    TEST_CASE_EVAL();

    TEST_CASE("Decrypt TDES");
    TEST_CHECK_ARRAY(tdes_decrypt_block(C, D, K3), M);
    TEST_CASE_EVAL();
}

